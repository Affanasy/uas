        <center><h2>Ar - Razi Ilmuwan Muslim Serba Bisa</h2></center>

        <p>
        Abu Bakar Muhammad bin Zakaria ar-Razi atau dikenali sebagai Rhazes di dunia barat merupakan salah seorang pakar sains Iran yang hidup antara tahun 864 - 930. Ia lahir di Rayy, Teheran pada tahun 251 H./865 dan wafat pada tahun 313 H/925.       
        </p>
				<br>
				<p>
        Ar-Razi sejak muda telah mempelajari filsafat, kimia, matematika dan kesastraan. Dalam bidang kedokteran, ia berguru kepada Hunayn bin Ishaq di Baghdad. 
        Sekembalinya ke Teheran, ia dipercaya untuk memimpin sebuah rumah sakit di Rayy. Selanjutnya ia juga memimpin Rumah Sakit Muqtadari di Baghdad.
        </p>
				<br>
				<p>
        Ar-Razi juga diketahui sebagai ilmuwan serbabisa[1] dan dianggap sebagai salah satu ilmuwan terbesar dalam Islam.        
        </p>         
