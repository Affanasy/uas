        <center><h2>Al - Farabi Guru Kedua Setelah Aristoteles</h2></center>

        <p>
          Abū Nasir Muhammad bin al-Farakh al-Fārābi adalah ilmuwan dan filsuf Islam berasal dari Farab, Kazakhstan.[6] Ia juga dikenal dengan nama Abū Nasir al-Fārābi
        </p>
				<br>
				<p>
          Ada yang mengatakan Al-Farabi sebagai pengikut Syi’ah Imamiyah, tetapi pendapat ini tidak kuat dan hanya didasarkan pada teks dalam salah satu karyanya yang mengatakan seorang filsuf-raja sama dengan seorang imam.
        </p>
				<br>
				<p>
          Al-Farabi dikenal dengan sebutan "guru kedua" setelah Aristoteles, karena kemampuannya dalam memahami Aristoteles yang dikenal sebagai guru pertama dalam ilmu filsafat
        </p>
        <br>
				<p>
          Dia adalah filsuf Islam pertama yang berupaya menghadapkan, mempertalikan dan sejauh mungkin menyelaraskan filsafat politik Yunani klasik dengan Islam serta berupaya membuatnya bisa dimengerti di dalam konteks agama-agama wahyu.
        </p>
				<br>
				<p>
          Al-Farabi hidup pada daerah otonomi di bawah pemerintahan Sayf al Dawla dan di zaman pemerintahan dinasti Abbasiyyah, yang berbentuk Monarki yang dipimpin oleh seorang Khalifah.
        </p>
				<br>
				<p>
        Ia lahir dimasa kepemimpinan Khalifah Mu’tamid (869-892 M) dan meninggal pada masa pemerintahan Khalifah Al-Muthi’ (946-974 M) di mana periode tersebut dianggap sebagai periode yang paling kacau karena ketiadaan kestabilan politik.
        </p>
        <br>
        <p>
        Dalam kondisi demikian, al-Farabi berkenalan dengan pemikiran-pemikiran dari para ahli Filsafat Yunani seperti Plato dan Aristoteles dan mencoba mengkombinasikan ide atau pemikiran-pemikiran Yunani Kuno dengan pemikiran Islam untuk menciptakan sebuah negara pemerintahan yang ideal (Negara Utama).   
        </p>
