        <center><h2>Al - Kindi Filsuf Pertama Dari Islam</h2></center>

        <p>
          Abu Yusuf Ya'qub bin'Isḥāq As-Sabbah al-Kindi adalah seorang Arab, Filsuf Muslim , polymath , matematikawan , dokter dan musisi.
          Al-Kindi adalah filosof Islam pertama yang bergerak, dan dipuji sebagai "bapak filsafat Arab".
        </p>
				<br>
				<p>
          Al-Kindi lahir di Kufah dan dididik di Bagdad. Ia menjadi tokoh terkemuka di Rumah Kebijaksanaan , 
          dan sejumlah khalifah Abbasiyah menunjuknya untuk mengawasi penerjemahan teks-teks ilmiah dan filosofis Yunani ke dalam bahasa Arab .
        </p>
				<br>
				<p>
          Kontak dengan "filsafat zaman dahulu" ini (sebagai filsafat Helenistik sering disebut oleh para sarjana Muslim) memiliki efek mendalam pada dirinya,
          saat ia mensintesis, mengadaptasi dan mempromosikan filsafat Helenistik dan Peripatetik di dunia Muslim .
				</p>
        <br>
				<p>
          Dia kemudian menulis ratusan risalah asli sendiri pada berbagai mata pelajaran mulai dari metafisika , etika, logika dan psikologi , kedokteran, farmakologi ,
          matematika, astronomi , astrologi dan optik , dan lebih jauh ke topik yang lebih praktis seperti parfum. , pedang, permata, kaca, pewarna, zoologi, pasang surut, cermin, meteorologi , dan gempa bumi .
        </p>
				<br>
				<p>
          Di bidang matematika, al-Kindi memainkan peran penting dalam memperkenalkan angka India ke dunia Islam, dan kemudian, diberi label ulang sebagai angka Arab , ke dunia Kristen , bersama dengan Al-Khawarizmi .
        </p>
				<br>
				<p>
          Namun terlepas dari peran penting yang ia mainkan dalam membuat filsafat dapat diakses oleh para intelektual Muslim, 
          keluaran filosofisnya sendiri sebagian besar dibayangi oleh al-Farabi dan sangat sedikit teksnya yang tersedia untuk dipelajari oleh para sarjana modern.
        </p>
